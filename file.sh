# 1. Crea el repositorio en github.com y luego:
git init
git add .
git commit -m "TracerCam v1.0"
git branch -M main
git remote add origin https://github.com/TU_USUARIO/tracercam.git
git push -u origin main

# 2. En GitHub: Settings > Pages > Branch: main > Save
# 3. URL lista: https://TU_USUARIO.github.io/tracercam/
